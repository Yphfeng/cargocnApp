		<view class="mainRow">2.2低热值气体</view>
		<view class="mainRow">低热值气体是指燃料气体完全燃烧后，其烟气被冷却到初始温度，其中的水蒸气仍以蒸汽的状态排出的燃料气体。如瓦斯、填埋气、沼气、其他低热值气体。</view>
		<view class="mainRow">2.3有毒气体</view>
		<view class="mainRow">常温常压下呈气态或极易挥发的有毒化学物。来源于工业污染，煤和石油的燃烧及生物材料的腐败分解。对呼吸道有刺激作用，亦易吸入中毒。包括氨、臭氧、二氧化氮、二氧化硫、一氧化碳、硫化氢及光化学烟雾等。</view>
		<view class="mainRow">3、易燃液体</view>
		<view class="mainRow">3.1 低闪点类液体</view>
		<view class="mainRow">低闪点液体是指闭杯试验闪点<—18℃的液体。如汽油、乙醚、丙酮等。</view>
		<view class="mainRow">3.2 中闪点类液体</view>
