		<view class="mainRow">一、危险货物</view>
		<view class="mainRow">危险货物是指容易引起燃烧、爆炸、腐蚀、中毒或有放射性的物品，在运输、储存过程中容易造成人身伤亡和财产损失，必须采用特殊防护设施与措施的货物。危险货物以《危险货物分类和品名编号》（GB6944)、《危险货物品名表》（GB12268)、《危险货物道路运输规则》（JT/T617-2018)、规范性法律文件所列为准，包括但不限于：</view>
		<view class="mainRow">1、爆炸品</view>
		<view class="mainRow">爆炸品，是指包括具有整体爆炸危险、具有抛射危险但无整体爆炸危险、具有燃烧危险并兼有较小爆炸（局部爆炸）或较小抛射（局部抛射）危险之一或兼有这两种危险但无整体危险、无重大危险、具有整体爆炸危险的极不敏感和不具有整体爆炸危险的极不敏感的物质、物品。</view>
