		<view class="mainRow">4.2 易自燃物质</view>
		<view class="mainRow">易自燃物质，是指在正常运输条件下易自发地放热或与空气接触发生温度升髙现象，从而易着火的物质。此类液体或固体物质的自燃点低，且能自行发热，当热量积聚使其温度到达自燃点时，不需要外界明火即能自行燃烧。如黄磷、未经抗氧剂处理的鱼粉等。虽较一级自燃物品稳定，但可因积热不散而发生自感，遇火星也可致燃，应贮阴凉、干燥、通风处所，温度不宜超过30-32℃，相对湿度要在75-80%以下，与氧化剂、氧、氯分贮。浸油金属屑应与酸类分开，以防致燃。严禁烟火，曝晒。应注意通风防潮，防止霉变。</view>
		<view class="mainRow">4.3 潮湿时放出易燃气体的物质</view>
		<view class="mainRow">遇水放出易燃气体物质，是指与水反应易自发成为易燃或放出到达危险数量的易燃气体的液体或固体物质。如碳化钙、磷化氢、钠、钾等。由于物质遇水后能发生剧烈的化学反应，放出易燃气体，并产生一定热量，所以当反应所产生的热量加热反应所产生的气体温度上升达到自燃点时或遇明火时立即燃烧甚至爆炸。由于含有不饱和键化合物(如桐油酸、亚麻酸等高级不饱和脂肪酸的甘油酯)，在潮湿和高温环境中易于产生自氧化作用与聚合作用，从而可引起自燃。</view>
		<view class="mainRow">5、氧化物质（氧化剂）与有机过氧化物</view>
		<view class="mainRow">5.1 氧化物质（氧化剂）</view>
		<view class="mainRow">氧化性物质，是指本身未必燃烧，但可释放出氧，可能引起或促使其他物质燃烧的一种化学性质比较活泼的物质。常指在无机化合物中含有高价态原子结构的物质和含有双氧结构的物质。其本身一般不会燃烧。但如果遇到酸或受潮湿、强热，或与其他还原性物质、易燃物质接触，即能进行氧化分解反应,放出热量和氧气,引起可燃物质的燃烧，有时还能形成爆炸性混合物。</view>
		<view class="mainRow">5.2 有机过氧化物</view>
		<view class="mainRow">特征是受热超过一定温度后会分解产生含氧自由基，不稳定、易分解。化工生产的有机过氧化物主要是用来作合成树脂的聚合引发剂、催化剂。在高分子材料领域，它可用作自由基聚合的引发剂、接枝反应的引发剂、橡胶和塑料的交联剂、不饱和聚酯的固化剂以及纺丝级聚丙烯制备中的分子量及分子量分布调节剂。环境中的污染空气在光作用下通过自由基反应可产生过氧酰基硝酸酯类化合物，是光化学氧化剂中的粒种之一。对皮肤、眼睛、粘膜有强烈的刺激性，是大气中的重要污染物。此类物质属于易燃易爆危险品，使用中应注意安全性。</view>
		<view class="mainRow">6、有毒的（毒性的）物质和感染性物质</view>
		<view class="mainRow">6.1 有毒的（毒性的）物质</view>
		<view class="mainRow">一般的定义为凡是以小剂量进入机体，通过化学或物理作用能够导致健康受损的物质。根据这一定义可知，有毒物质是相对的，剂量决定着一种成分是否有毒。在被吞食、吸入或与皮肤接触后可能造成死亡或严重受伤或损害人体健康的物质。归入这一小类的是在常温常压条件下呈液态或固态的物质，有些还具有燃烧、腐蚀等特性。有毒物质的毒性主要用半数致死量LD50(half-lethal dose)或半数致死浓度LC50(half-lethal concentration)来度量。毒物的LD50值或LC50值越小，毒性越大。</view>
		<view class="mainRow">6.2 感染性物质</view>
		<view class="mainRow">感染性物质，是指含有已知或有理由认为会使动物或人生病甚至死亡的活性微生物的物质。包括细菌、病毒、立克次氏体、寄生虫、真菌、重组体、交杂体、突变体，但不包括以基因改变的微生物和生物体（“国际危规”将其归为第9类）。感染性物质是指已知或一般有理由相信含有病原体的物质。所谓病原体是指已知或有理由相信会使人或动物引起感染性疾病的微生物（包括细菌、病毒、立克次氏体、寄生生物、真菌）或微生物重组体（杂交体或突变体）</view>
		<view class="mainRow">7、放射性物质</view>
		<view class="mainRow">某些物质的原子核能发生衰变，放出我们肉眼看不见也感觉不到，只能用专门的仪器才能探测到的射线，物质的这种性质叫作放射性。 放射性物质是那些能自然的向外辐射能量，发出射线的物质。一般都是原子质量很高的金属，像钚 ，铀，等。放射性物质放出的射线主要有α射线、β射线、γ射线、正电子、质子、中子、中微子等其他粒子。</view>
		<view class="mainRow">8、腐蚀性物质</view>
		<view class="mainRow">狭义的腐蚀是指金属与环境间的物理和化学相互 腐蚀的钢管作用，使金属性能发生变化，导致金属,环境及其构成 被大气污染腐蚀的现象。腐蚀是一种物理电化学变化，腐蚀分很多情况，如硫酸的是将被腐蚀物体中的H、O以2：1的比例脱出，因H2O的分子中H、O的比例也为2：1，故硫酸的腐蚀性又称脱水性；又如NaOH的腐蚀，它会破坏被腐蚀物体的蛋白质，从而产生腐蚀效果的。如硝酸、硫酸、氢氯酸、氢溴酸、氢碘酸、高氯酸，还有由1体积的浓硝酸和3体积的浓盐酸混合而成的王水等酸性腐蚀，还有氢氧化钠等碱性腐蚀品</view>
